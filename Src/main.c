  uint8_t isc_erase_fpga[4] = {0x0E, 0x00,0x00,0x00};
  uint8_t status_reg_fpga[4] = {0x3C, 0x00,0x00,0x00};
  uint8_t lsc_burst_fpga[4] = {0x7A, 0x00,0x00,0x00};
